package pojo;

public class StudentInfo {
        private String name1;
        private String sex;
        private String csdate;
        private String idcard;
        private String username;
        private String minzhu;
        private String zzmm;
        private String dszn;
        private String phone;
        private String qq;
        private String email;
        private String name2;
        private String tel2;

    public StudentInfo() {
    }

    public StudentInfo(String name1, String sex, String csdate, String idcard, String username, String minzhu, String zzmm, String dszn, String phone, String qq, String email, String name2, String tel2) {
        this.name1 = name1;
        this.sex = sex;
        this.csdate = csdate;
        this.idcard = idcard;
        this.username = username;
        this.minzhu = minzhu;
        this.zzmm = zzmm;
        this.dszn = dszn;
        this.phone = phone;
        this.qq = qq;
        this.email = email;
        this.name2 = name2;
        this.tel2 = tel2;
    }

    public String getName1() {
        return name1;
    }

    public void setName1(String name1) {
        this.name1 = name1;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getCsdate() {
        return csdate;
    }

    public void setCsdate(String csdate) {
        this.csdate = csdate;
    }

    public String getIdcard() {
        return idcard;
    }

    public void setIdcard(String idcard) {
        this.idcard = idcard;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getMinzhu() {
        return minzhu;
    }

    public void setMinzhu(String minzhu) {
        this.minzhu = minzhu;
    }

    public String getZzmm() {
        return zzmm;
    }

    public void setZzmm(String zzmm) {
        this.zzmm = zzmm;
    }

    public String getDszn() {
        return dszn;
    }

    public void setDszn(String dszn) {
        this.dszn = dszn;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getQq() {
        return qq;
    }

    public void setQq(String qq) {
        this.qq = qq;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName2() {
        return name2;
    }

    public void setName2(String name2) {
        this.name2 = name2;
    }

    public String getTel2() {
        return tel2;
    }

    public void setTel2(String tel2) {
        this.tel2 = tel2;
    }
}
